package walgreens.ecom.batch.framework.common.beans;


import java.util.Map;



public class OrderBean {
    
    private Map<String,ProductBean> productsMap = null;
    private String cartTotal = null;
    private String cartCalculatedTax = null;
    private String scenarioId = null;
    private String orderConfirmationNumber = null;

    public String getScenarioId() {
        return scenarioId;
    }
    public void setScenarioId(String scenarioId) {
        this.scenarioId = scenarioId;
    }
    public String getOrderConfirmationNumber() {
        return orderConfirmationNumber;
    }
    public void setOrderConfirmationNumber(String orderConfirmationNumber) {
        this.orderConfirmationNumber = orderConfirmationNumber;
    }
  
    public String getCartTotal() {
        return cartTotal;
    }
    public void setCartTotal(String cartTotal) {
        this.cartTotal = cartTotal;
    }
    public String getCartCalculatedTax() {
        return cartCalculatedTax;
    }
    public void setCartCalculatedTax(String cartCalculatedTax) {
        this.cartCalculatedTax = cartCalculatedTax;
    }
    public Map<String,ProductBean> getProductsMap() {
	return productsMap;
    }
    public void setProductsMap(Map<String,ProductBean> productsMap) {
	this.productsMap = productsMap;
    }


}
